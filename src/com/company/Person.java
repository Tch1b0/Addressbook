package com.company;

public class Person {

    String name = "a";
    int age = 0;

    void setName(String name){
        this.name = name;
    }
    String getName() {
        return this.name;
    }
    void setAge(int age){
        this.age = age;
    }
    int getAge(){
        return this.age;
    }

}
